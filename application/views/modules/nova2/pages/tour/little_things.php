<h4>
	Upload Your Images
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>Use a simple web form to upload character, tour, mission and award images to the server and then use them in your pages.</p>

<h4>
	Image Galleries
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>Missions, tour items and characters will display images in a mini gallery format. Click on the image and you can move through all the images with your mouse or keyboard!</p>

<h4>
	Remember Me
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>Gone are the days of signing in every time you come to the site with a remember me option. Nova will keep you logged in to the site for 2 weeks before requiring a login again. (Requires cookies to be enabled.)</p>

<h4>
	Private Messaging Improvements
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>Send private messages to multiple people, forward private messages to people and reply to everyone involved with the private message with only a couple of clicks.</p>

<h4>
	Mission Groups
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>Missions groups given admins the ability to group missions together by arc, season, topic or any other criteria they want and display them together on a single page.</p>

<h4>RSS Feeds</h4>
<p>Provide RSS feeds for users and guests to track your mission posts, personal logs and news items through a feed reader like Google Reader.</p>

<h4>
	Search
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>Quickly find a mission post, personal log, news item or wiki page by title or content with Nova's built-in search feature.</p>

<h4>Improved Security</h4>
<p>Security takes top billing in Nova with protection against XSS attacks, better protection against SQL injection attacks, limiting login attempts to protect against brute force attacks and heavier password hashing to make sure your information is always secure.</p>

<h4>
	Maintenance Mode
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>Keep people out of the site while you're making updates with the new maintenance mode.</p>

<h4>
	New Access Control
	&nbsp;
	<a href="#" class='tip' tooltip='<?php echo img($images['view']);?>'><?php echo img($images['view']);?></a>
</h4>
<p>A brand new, role-based access system allows you to group users into groups for access or create a group for one person. Fine-grained control means you can contral exactly what a user can and can't do in the system.</p>

<h4>User Interface Improvements</h4>
<p>Countless user interface improvements make Nova a pleasure to use. Sliders for a position's open slots, drag-n-drop sortable items, loading graphics, progress bars and much more enhance Nova and make it feel like a top-notch web application.</p>

