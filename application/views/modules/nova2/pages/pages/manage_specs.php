<p>For RPGs that use a vessel of some sort or have something that would require specifications to be shown, Nova has built-in specifications management features. Nova gives admins unparalleled control over their specs form to make it whatever they want. With an easy-to-use interface, admins can customize the specs form and then add data to it without a second thought.</p>

<p>The Specs management page is a straightforward form: fill out the fields and click Submit. Nova will only update fields that have been changed and will mark when those fields were changed in the database. Once the changes have been submitted, they can be viewed on the <?php echo anchor('nova/pages/sim/specs', 'Specifications Listing');?> page.</p>

<h4>Permissions</h4>

<p>In order to manage specifications, you need to have <kbd>manage/specs</kbd> permissions.</p>