<p>Sim types are used by Nova primarily for menu items. If you have your RPG set up as a ship and you want to change to a base, Nova will automatically turn off any menu items that have a ship specific flag set and turn on any menu items that have a base specific flag set on them. By default, Nova comes with a wide variety of sim types to choose from, but you can add new types, edit the existing ones or even delete types you will never need (though there shouldn&rsquo;t be any need to delete types).</p>

<h4>Permissions</h4>

<p>In order to manage access roles, a user needs to have <kbd>site/simtypes</kbd> permissions.</p>

<h2>Adding a Sim Type</h2>

<p>To create a new sim type, simply click on Add Sim Type. A hidden field will become visible and let you add a name (should be entered in lowercase). After clicking Add, the type will be added at the end of the list. Once you have added a type, it will be available to use in menu item management.</p>

<h2>Editing a Sim Type</h2>

<p>Nova gives you the ability to edit all the sim types at the same time in the event you need to change several items. Simply update all the fields you want to update and click Update. Nova will go through and make the changes to all the items. It&rsquo;s possible to update and delete items at the same time.</p>

<h2>Deleting a Sim Type</h2>

<p>Like editing a sim type, Nova lets you delete several sim types at the same time. All you have to do is click the checkbox next to the sim type you want to delete. Once you have selected all the items you want to delete, click Update and Nova will delete the items you checked. It&rsquo;s possible to update and delete items at the same time.</p>

<p class="critical"><strong>Warning:</strong> Use caution when deleting sim types as it will affect the entire system!</p>