<p>For RPGs that use a vessel or some type of location and want the ability to give players an idea of the layout, Nova comes with a built-in deck listing feature. Using the features on this page, you can administer the listing in a few easy steps, whether you&rsquo;re adding to it, taking away from it, or just updating it.</p>

<h4>Permissions</h4>

<p>In order to manage the deck listing, you need to have <kbd>manage/decks</kbd> permissions.</p>

<h2>Adding a Deck</h2>

<p>To add a deck to the listing, simply use the input box near the top of the page to name the deck. Once you&rsquo;ve named the deck, click <strong>Add</strong> and Nova will add the item to the listing in the middle of the page.</p>

<h2>Editing a Deck</h2>

<p>To edit a deck, simply click on the words on the respective bar and a pop-up window will apear with the ability to change name or the content of the deck. Once you&rsquo;re satisified with your changes, click <strong>Submit</strong> and Nova will update the record. If you want to change the order of the decks, you can do so by simply clicking and dragging the items around until you have an order that you like. Once you&rsquo;ve finished re-ordering the decks, clicking <strong>Update</strong> and Nova will lock your new order in to the database.</p>

<h2>Deleting a Deck</h2>

<p>To delete a deck from the listing, simply click on the X on the respective bar and Nova will remove the item from the deck listing.</p>