<h1>State of Anodyne Productions &ndash; January 2009</h1>

<p>This document serves to provide the state of Anodyne Productions and its various projects as well as lay out goals for the coming calendar year.</p>

<p>It's amazing to see what a single year brings (and doesn't bring). 2008 had its share of victories and defeats and we're excited to see what 2009 brings!</p>

<h2>SIMM Management System</h2>

<p>It's been a busy year for SMS, but with the year complete and no major released planned, it's likely that SMS will start to slow down significantly in the coming year.</p>

<p>In terms of releases, it's been a steady year. In 2008, Anodyne finally shipped version 2.6 of the system, and to date, have released 7 patches. There are additional patches planned, but they'll bring minor bug fixes and system tweaks, nothing nearly as extensive as the 2.6 release. Right now, SMS is probably more stable than it's ever been and we're still seeing a lot of new people starting to use SMS, a fact that encourages us as we move toward Nova.</p>

<p>From here, it is unlikely that SMS will attract much attention. With a handful of updates planned for 2009 and some official MODs, our focus is shifting from SMS to Nova almost exclusively.</p>

<p>I do want to take a few minutes and thank all the people who have worked so hard on SMS for the last 3 years. We've all put blood, sweat and tears into this product and it shows. Much thanks to Jon Matterson for his unwavering support over the years. He had a vision for SMS 2 and as he's told me several times, SMS has surpassed his original vision. We never would have been able to do that if not for the original product he labored over and we all came to love. Thanks to Nate Wharry for shouldering the Anodyne site hosting needs until recently. It was an awesome gesture and ensured that Anodyne had a virtual home. Mathew Edison and Brian Smith have rocked with their support help and QA efforts. It isn't easy testing a system like SMS, but they've done a great job. And to everyone else who's helped, thank you for everything over the last year and especially over the last few years!</p>

<h2>Nova</h2>

<p>The last quarter for us was big because it was the first time we'd spent more time working on Nova than on SMS. It's definitely bittersweet to be changing products, but it's time. In 2008, we officially announced the name for SMS 3 and opened up a preview site for all things Nova (http://nova.anodyne-productions.com). Everyone's been very curious about what we're doing with Nova and we've worked hard to make as much information available as possible. As we move forward, lots more information will be available as well.</p>

<p>During the final quarter of 2008, we worked on the second development milestone (M2) of Nova with great success. We're now only about a week or two away from declaring M2 complete and moving on to M3. Our focus with M2 was getting the un-authenticated system working and laying the foundation for lots more great work to come. Major features added during M2 include:</p>

<ul>
	<li>system search</li>
	<li>separating player and character bios</li>
	<li>dynamic forms for character bios/join page, specs, and tour items</li>
	<li>sub departments on the manifest</li>
	<li>a new way to view awards</li>
	<li>HTML emails</li>
	<li>commenting on posts, logs, and new items</li>
</ul>

<p>As we move out of M2 and into M3, our focus shifts to the bulk of the system that players see once they're logged in. Authentication and user access control are the first things to tackle in M3. After that, we'll shift our focus to posting to the system as well as major improvements to private messaging. We're not sure how long that will take, but we expect to wrap up development by the end of 2009 with M4 focusing on site management features, M5 focusing on reporting, and M6 focusing on user and character stuff. In addition to those, we're going to tackle a mini-wiki that's being called Thresher (as of right now). There will be lots of information available on those milestones as we get closer.</p>

<p>As always, make sure to keep checking the message boards and the Nova preview site for the most up-to-date information about Nova.</p>

