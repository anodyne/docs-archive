<p>Awards are a great way to reward and recognize players for their accomplishments, both in character and out of character. Nova comes with a system that allows admins to create awards and issue them to members of the crew with little to no effort. </p>

<h3>Permissions</h3>

<p>In order to manage awards, you need to have <kbd>manage/awards</kbd> permissions.</p>

<h2>Adding an Award</h2>

<p>To add an award to Nova, simply click on the <strong>Add Award</strong> link near the top of the page. You will be redirected to a page with two tabs: info and images. In the info tab, fill out all of the fields. When you've completed the fields in the info tab, click on the image tab and select your images from a list of uploaded images (if you have any) or use the text field to manually put the name of the image in the field. Once you're done, click <strong>Submit</strong> and Nova will create the award and you'll be able to give it to characters.</p>

<h2>Editing an Award</h2>

<p>To edit an award in Nova, simply click on the edit icon to the right of the award information. You will be redirected to a page with two tabs: info and images. In the info tab, update all of the fields you want. When you've finished updating the fields in the info tab, click on the images tab and select your image from a list of uploaded images (if you have any) or use the text field to manually put the name of the image in the field. Once you're done, click <strong>Submit</strong> and Nova will update the award.</p>

<h2>Deleting an Award</h2>

<p>To delete an award from Nova, simply click on the delete icon to the right of the award information. A modal window will appear and ask you to confirm you want to delete the award. If you do, click <strong>Submit</strong> and Nova will delete the award. If you don't want to delete the award, you can click anywhere outside of the modal window, click the X or hit the Escape key to abort the delete process.</p>

<p class="alert alert-danger"><strong>Warning:</strong> Deleting awards is permanent and cannot be undone!</p>