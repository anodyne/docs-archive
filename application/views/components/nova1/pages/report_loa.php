<p>Nova provides the ability to track LOA requests over time along with some statistics about the LOAs. This can be helpful to know if a certain player has a habit of claiming a short LOA than they really take or verying a reason for an LOA if you accidentally delete an email.</p>

<p>The report is incredibly straightforward. You will be shown a list of LOA records along with data about:</p>

<ul>
	<li>Who took the LOA</li>
	<li>When the LOA started</li>
	<li>When the LOA ended (if it hasn't ended, this won't be shown)</li>
	<li>The reason for the LOA</li>
	<li>Stated duration</li>
	<li>Actual duration</li>
</ul>

<p class="alert alert-info"><strong>Note:</strong> At this time, there is no ability to filter by user or sort the data. We hope to bring this functionality in a future update.</p>