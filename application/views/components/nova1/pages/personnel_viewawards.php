<p>Over time, the number of awards someone can win on the RPG has the potential to bog down the bio page. To remedy this issue, we've created a separate page in Nova that holds all awards.</p>

<h2>Viewing Awards for a Player</h2>

<p>This page provides a breakdown, by character, of the awards won by each player. If an award is not associated with a character (i.e. an OOC award), it will be displayed at the top of the page apart from all the character awards. Clicking on an award image will take a user to that award's detail page. In addition to the name of the award, the page also displays when the award was given, the reason it was given and who nominated the player for the award.</p>

<p>Viewing a player's awards is triggered by a parameter of <dfn>p</dfn> in the third URI segment followed by the player ID.</p>

<h2>Viewing Awards for a Character</h2>

<p>This page provides a breakdown of the awards won by a character. Clicking on an award image will take a user to that award's detail page. In addition to the name of the award, the page also displays when the award was given, the reason it was given and who nominated the character for the award.</p>

<p>Viewing a character's awards is triggered by a parameter of <dfn>c</dfn> in the third URI segment followed by the character ID.</p>