<p>The bio form is almost identical to the join form, the only difference being that the fields will be populated with the character data instead of blank. You can edit any information shown on the page and when you're satisified with your changes can click <strong>Submit</strong> to have Nova update the database. Changes made to bios are immediately available on the character bio pages.</p>

<h3>Permissions</h3>

<p>In order to edit bios, you need to have <kbd>characters/bio</kbd> permissions with either access level 1, 2 or 3. Level 1 allows a user to edit the bio(s) of their own characters (playing and non-playing). Level 2 allows a user to edit the bios of their own characters or any NPC in the system. Finally, level 3 allows a user to edit any bio in the system.</p>

