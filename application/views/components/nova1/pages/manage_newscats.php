<h3>Permissions</h3>

<p>In order to manage news categories, you need to have <kbd>manage/newscats</kbd> permissions.</p>

<h2>Adding a News Category</h2>

<p>To add a news category to Nova, simply click on the <strong>Add News Category</strong> link near the top of the page. A field will appear below the link where you can type the name of your category. Clicking <strong>Add</strong> will add the category to Nova and will be available for use when users write news items.</p>

<h2>Editing a News Category</h2>

<p>Like many of the other editing components in Nova, you can update multiple news categories simultaneously. To do so, simply make your changes to the name or display status and click <strong>Update</strong>. Nova will go through and make all the changes to the news categories.</p>

<p class="alert alert-danger"><strong>Warning:</strong> Updating news categories will affect any news items in that category.</p>

<h2>Deleting a News Category</h2>

<p>Deleting a news category in Nova is as easy as clicking the delete checkbox on all the categories you want to delete and clicking <strong>Update</strong> at the bottom of the page. After Nova finishes updating the categories, it will go through and delete the marked categories as well.</p>

<p class="alert alert-danger"><strong>Warning:</strong> Deleting news categories is permanent and cannot be undone! It will affect any news items in that category.</p>