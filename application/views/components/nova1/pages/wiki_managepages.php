<p>Thresher organizes all its pages into categories to make them easy to find. Unlike a traditional wiki that will let you create categories on the fly, Thresher requires a category to exist before it can be used for categorizing a wiki page.</p>

<p>The listing of pages has been kept as simple as possible. Included in the list is the name of the page from it's latest draft, who created the page and if there have been any updates, who made the updates. Finally, to the right are icons for editing and deleting.</p>

<p>Because of the potential for this page to get long, we've also included a quick search feature that allows you to quickly and easily filter results based on what you type. To use the quick search, simply begin typing in the search box above the pages and Thresher will filter the results that you want.</p>

<h3>Permissions</h3>

<p>In order to manage categories in Thresher, a user needs to have <kbd>wiki/categories</kbd> permissions.</p>

<h2>Adding a Page</h2>

<p>Creating a new wiki page can be done by clicking on the <strong>Create New Wiki Page</strong> link at the top of the page. The user will be redirected to the page creation screen where they can create their page.</p>

<h2>Editing a Page</h2>

<p>Editing a page is just like creating a page. Clicking on the edit icon will redirect the user to the edit page screen where they can make changes to the page and submit those to the database. Because of the versioned nature of Thresher, editing a page is actually a two-step process (though the user only sees one of those steps). When an edit is submitted, Thresher will create a new draft of the page that includes all the changes. That draft is then associated with the page record. This process ensures that pages can be reverted to a previous version of the page with a single click.</p>

<h2>Deleting a Page</h2>

<p>Deleting a page is only a matter of clicking on the delete icon and confirming you want to delete the page by clicking <strong>Submit</strong>. If you decide that you don't want to delete the page, you can get out of the modal window by either clicking the X in the upper corner, hitting escape on your keyboard or clicking anywhere outside the window.</p>