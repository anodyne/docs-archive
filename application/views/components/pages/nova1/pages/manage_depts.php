<p>Departments in Nova are responsible for holding the positions characters are assigned to and help to organize characters into their respective, natural sections. Like other features in Nova, it's possible to create your own departments, edit existing departments and delete departments. In addition, Nova also allows each department to contain an unlimited number of sub-departments to further organize your departments and positions*.</p>

<h3>Permissions</h3>

<p>In order to manage departments, you need to have <kbd>manage/depts</kbd> permissions.</p>

<h2>Adding a Department</h2>

<p>To add a department to Nova, simply click on the <strong>Add Department</strong> link near the top of the page. A pop-up window will appear with all the fields needed for creating a new department. Once you have filled out all the fields, click submit and Nova will create the department for you. Once the department has been created, positions can be assigned to it from the Position Management page.</p>

<h2>Editing a Department</h2>

<p>To edit a department in Nova, all you need to do are change the fields you want on the Department Management page and click Update at the bottom. You can change as many departments as you want at the same time and Nova will update all of them simultaneously.</p>

<h2>Deleting a Department</h2>

<p>To delete a department in Nova, simply click on the delete button to the right of the department in the listing. A pop-up window will appear, warning you the change you're about to make is permanent and then will present you with several options.</p>

<p>The first option you're given is to either delete all positions in that department or reassign the positions to another department. Like deleting a department, removing all positions is permanent and cannot be undone! If the department you're deleting has sub-departments, you'll be given a second dropdown menu to decide where to reassign those departments to. Selecting <dfn>none</dfn> will make all the sub-departments parent level departments, otherwise, the sub-departments will be moved to the new department you select.</p>

<p class="critical"><strong>Warning:</strong> Deleting departments is permanent and cannot be undone!</p>

<br />
<p class="fontSmall"><em>*At this time, there is no way to create nested sub-departments (children of children).</em></p>