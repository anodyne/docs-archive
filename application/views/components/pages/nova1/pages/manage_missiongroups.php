<p>Mission groups are a new way to organize missions to group related missions together to make it easier for users to move through mission archives. Game masters can choose to group mission together by arc, season, topic or anything else they want. By simply select the proper mission group from the mission management page, missions will be organized together regardless of whether they're consecutive missions or not.</p>

<h3>Permissions</h3>

<p>In order to manage mission groups, you must have <kbd>manage/missions</kbd> permissions.</p>

<h2>Adding a Mission Group</h2>

<p>Add a new mission group is as simple as clicking on the Add Mission Group link at the top of the page. A new section will appear with fields for the name of the group, the order and the group's description. Once you've filled the information out, simply click Add and the group will be added to the database and available to use on the mission management page.</p>

<h2>Editing Mission Groups</h2>

<p>To update a mission group, simply make the changes to any of the items you want and click Update. Like other places in Nova, the system will gather all your changes together from any of the mission groups and update the database at the same time.</p>

<p>To delete a mission group, simply click the delete checkbox next to the item. When you click update, any items that were checked will be removed from the database.</p>