<p>Thresher organizes all its pages into categories to make them easy to find. Unlike a traditional wiki that will let you create categories on the fly, Thresher requires a category to exist before it can be used for categorizing a wiki page.</p>

<h3>Permissions</h3>

<p>In order to manage categories in Thresher, a user needs to have <kbd>wiki/categories</kbd> permissions.</p>

<h2>Adding a Category</h2>

<p>Creating a category can be accomplished by clicking on the <strong>Add a Category</strong> link at the top of the page. A simple text box will appear where you can type the name of the category and then click the <strong>Add</strong> button to add the category to the database.</p>

<h2>Editing a Category</h2>

<p>Like most other components in Nova, you can easily edit a category in Thresher by clicking on the edit icon and making your changes in the pop-up window that appears. Clicking <strong>Submit</strong> will complete the update and the changes will be seen immediately for that category.</p>

<h2>Deleting a Category</h2>

<p>Deleting a category is only a matter of clicking on the delete icon and confirming you want to delete the category by clicking <strong>Submit</strong>. If you decide that you don't want to delete the category, you can get out of the modal window by either clicking the X in the upper corner, hitting escape on your keyboard or clicking anywhere outside the window.</p>

<p class="critical"><strong>Warning:</strong> Deleting categories will affect any wiki pages that have that category. We recommend you manually clear out a category of its pages before deleting the category.</p>