<h2>Backup</h2>

<p>Before you attempt to update Nova 2, please make sure you backup both your files and database. While we don't anticipate any problems, if something does happen, you'll have a solid backup of your system to fall back to.</p>

<h2>Remove</h2>

<p>Once you've finished backing up all of your stuff, delete the <code>nova</code> directory in its entirety.</p>

<p class="alert alert-danger">When we say delete, we mean it. Delete the directory and <strong>then</strong> upload the new copy.</p>

<h2>Upload</h2>

<p>With the <code>nova</code> directory deleted, upload the directory from the zip archive you downloaded from the Anodyne site.</p>

<h2>Update</h2>

<p>The update process works just like the update process in Nova 1. The first step will try to do an automatic backup for you, but you don't have to worry about that too much since you manually backed up everything before you started. (You did back up everything before you started, right?)</p>

<p>Let the update process do its thing and when you're done, you'll be back on the front Nova page and ready to use Nova again.</p>