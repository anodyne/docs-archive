<h1>Nova 3</h1>

<p>At Anodyne, we're never content to sit still and we're always looking for ways to make our products better, faster, smarter and easier to use. Nova 3 is the culmination of everything we've learned since 2005 about RPG management. Game masters want more control and something that lets them get in, get out and get back to the game. Our goal with Nova 3 is "smarter and better." With every decision we're making, we're asking ourselves <em>does this make Nova better? does this make Nova smarter?</em></p>

<p>Make no mistake, there are huge changes coming in Nova 3, but those changes will make Nova even easier to use and give game masters more control than they've had before. Like we did during development of Nova 1, we'll be pushing regular preview releases out to the community for testing and to give people a glimpse at the future of Nova. You can use the link below to get the latest preview release of Nova 3 and check out the initial user guide offerings.</p>

<p class="alert alert-danger">Nova 3 is alpha software, meaning it's incomplete and riddled with bugs. While we encourage people to check out Nova 3 and help us track down issues, it is <strong>not</strong> stable enough for use on a live site.</p>

